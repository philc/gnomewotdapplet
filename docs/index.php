<h2>
Overview
</h2>
<p>
This software project aims to provide a word of the day in the user's GNOME panel to aid in vocabulary development. Words can be retrieved from distributed dictionaries (such as dictionaries targetted towards SAT and GRE vocabulary), user-defined dictions, and online "word of the day" websites. It is open source software, licensed under the <a href="http://www.gnu.org/copyleft/gpl.html">GNU GPL</a>.
</p>

<h2>
About the Software
</h2>
<p>
The GNOME WOTD Applet is written in C#, developed and tested using the <a href="http://www.mono-project.com">Mono</a> .NET runtime. The authors of this software are <a href="mailto:philc--REMOVE--THIS--TEXT--@philisoft.com">Phil Crosby</a> and <a href="mailto:mikeq--REMOVE--THIS--TEXT--@wam.umd.edu">Michael Quinn</a>.
